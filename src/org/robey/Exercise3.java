package org.robey;

public class Exercise3 {
	
	/*
	 * Learning Java: Exercise 3
	 * Using Comments
	 */

	public static void main(String[] args) {
		System.out.println("Comments are handy.");
		System.out.println("");
		System.out.println("You can use single line comments by entering the following:");
		System.out.println("//Comment goes here");
		// This is a comment. It will not appear/output to the console.
		// Comments are useful to describe what is going on with your code.
		System.out.println("");
		System.out.println("You can use multi-line comments (block comment code) as well.");
		System.out.println(" - Start your block comment by entering: /*");
		System.out.println(" - End your block comment by entering: */");
		System.out.println(" - To create a new comment line within a block comment, hit enter.");
		System.out.println(" - Each new line of block commenting must start with a *");
		System.out.println("Fortunately, Eclipse automatically places a * for your at the beginning of each new line of block commenting.");
		System.out.println("");
		System.out.println("Example:");
		System.out.println("/* Start of block comment");
		System.out.println("* Block comment");
		System.out.println("* Block comment");
		System.out.println("End of block comment */");
		/* This is block commenting.
		 * Hit enter to drop down to the next line of block comment.
		 * Each new line of block commenting starts with a *.
		 * Eclipse automatically places a * on each new line for you.
		 * Now let's close out this block comment.
		*/	
		
		
	}

}
